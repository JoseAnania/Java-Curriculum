    private void cargarLista()
    {
        ArrayList<AspiranteDto>lista=C.listado();
        DefaultTableModel modelo=new DefaultTableModel();
        String[]columnas=new String[]{"Puesto","Experiencia","Aspirante","Sueldo","Antiguedad"};
        modelo.setColumnIdentifiers(columnas);
        for (AspiranteDto A : lista) {
            Object[]filas=new Object[]{A.getNombreP(), A.getExperiencia(), A.getNombreA(), A.getSalario(), A.getAntiguedad()};
            modelo.addRow(filas);
        }
        tblListado.setModel(modelo);
    }

