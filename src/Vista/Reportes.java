     private void cargarLista()
    {
        ArrayList<NacionalidadDto>lista=C.Reporte1();
        DefaultTableModel modelo=new DefaultTableModel();
        String[]columnas=new String[]{"Nacionalidad","Cantidad"};
        modelo.setColumnIdentifiers(columnas);
        for (NacionalidadDto N : lista) {
            Object[]filas=new Object[]{N.getPais(),N.getCantidad()};
            modelo.addRow(filas);
        }
        tblReporte1.setModel(modelo);
    }

