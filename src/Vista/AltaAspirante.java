    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        int idA=0;
        String nombreA=txtNombre.getText();
        int idP=(int) cboPuesto.getSelectedIndex();
        int antiguedad=Integer.parseInt(txtAntiguedad.getText());
        float salario=Float.parseFloat(txtSalario.getText());
        int idN=(int) cboNacionalidad.getSelectedIndex();
        
        Aspirante A=new Aspirante(idA, nombreA, idP, antiguedad, salario, idN);
        C.agregarAspirante(A);
        limpiarCampos();
    }//GEN-LAST:event_btnAgregarActionPerformed

