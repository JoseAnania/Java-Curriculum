    private void llenarComboP()
    {
        ArrayList<Puesto>lista=C.comboP();
        DefaultComboBoxModel modelo=new DefaultComboBoxModel();
        modelo.addElement("Seleccione");
        for (Puesto P : lista) {
            modelo.addElement(P.getNombreP());
        }
        cboPuesto.setModel(modelo);
    }
    
    private void llenarComboN()
    {
        ArrayList<Nacionalidad>lista=C.comboN();
        DefaultComboBoxModel modelo=new DefaultComboBoxModel();
        modelo.addElement("Seleccione");
        for (Nacionalidad N : lista) {
            modelo.addElement(N.getPais());
        }
        cboNacionalidad.setModel(modelo);
    }
    
    private void limpiarCampos()
    {
        txtAntiguedad.setText("");
        txtNombre.setText("");
        txtSalario.setText("");
        cboNacionalidad.setSelectedIndex(0);
        cboPuesto.setSelectedIndex(0);
    }

